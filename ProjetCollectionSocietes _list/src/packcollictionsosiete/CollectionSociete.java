    private void txtrKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtrKeyPressed
        
        if(evt.getKeyChar() == '\n'){
           if(txtd.getText().compareTo("")!=0 && txtr.getText().compareTo("")!=0){
              float d =Float.parseFloat(txtd.getText());
              float r = Float.parseFloat(txtr.getText());
              float ben= calculerBenefice(d,r);
              String impo = String.valueOf(calculerImpot(ben));
              txtb.setText(String.valueOf(ben));
              txti.setText(impo);
           }else{
               JOptionPane.showMessageDialog(this, "Remplir les zone de text depence et recette","Erreur de saisi",JOptionPane.OK_OPTION);
            }
            
        }
    }//GEN-LAST:event_txtrKeyPressed

