    public float calculerBenefice(float dep,float rec){
        return rec -dep;
    }
    public float calculerImpot(float ben){
        float imp=0;
        if(ben <= 300000){
            imp = ben *0.15f;
        }else if(ben <= 1000000){
            imp = ben *0.20f;
        }else if(ben <= 100000000){
            imp = ben *0.255f;
        }else {
            imp = ben *0.33f;
        }
        return imp;
    }
    public void nouveau(){
        cmbns.setSelectedIndex(-1);
        txtnoms.setText("");
        txtsa.setText(null);
        txtd.setText(null);
        txtr.setText(null);
        txtb.setText(null);
        txti.setText(null);
        cmbns.requestFocus();
    }
    public void ajouter(List s,Societe soc){
        
        soc.ns=cmbns.getSelectedItem().toString();
        soc.noms=txtnoms.getText();
        soc.sa=txtsa.getText();
        soc.dep=Float.parseFloat(txtd.getText());
        soc.rec=Float.parseFloat(txtr.getText());
        soc.ben=Float.parseFloat(txtb.getText());
        soc.imp=Float.parseFloat(txti.getText());
        s.add(soc);
    }
    public Societe rechercher(List s,String num){
       Societe soc = null;
        Iterator it = s.iterator();
       while(it.hasNext()){
           Societe sc = new Societe();
           sc = (Societe)it.next();
           if(sc.ns.compareTo(num) == 0){
               soc = sc;
               break;
           }
           
       }
       return soc;
    }
    public void lister(List s){
       ((DefaultTableModel)tsociete.getModel()).setRowCount(0);
        Iterator it = s.iterator();
       while(it.hasNext()){
           Societe sc = new Societe();
           sc = (Societe)it.next();
           ((DefaultTableModel)tsociete.getModel()).addRow(new Object[]{sc.ns,sc.noms,sc.sa,sc.ben,sc.imp});

       }
       
    }
    public void modifier(List s, String num){
       Societe soc = new Societe();
       
        Iterator it = s.iterator();
       while(it.hasNext()){
           Societe sc = new Societe();
           sc = (Societe)it.next();
           if(sc.ns.compareTo(num) == 0){
              
               s.remove(sc);
               ajouter(s, soc);
               lister(s);
                JOptionPane.showMessageDialog(this, "Modification est bein enregistre","Modification est bein enregistre",JOptionPane.OK_OPTION);
               break;
           }
       }
    }
    public void supprimer (List s, String num){
        Iterator it = s.iterator();
        while(it.hasNext()){
          Societe sc = new Societe();   
          sc =(Societe)it.next();
          if(sc.ns.compareTo(num)==0){
              int rep = JOptionPane.showConfirmDialog(this, "Etes-vous sur de supprimer cette societe:","Suppression",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
              if(rep == JOptionPane.YES_OPTION){
                    s.remove(sc);
                    lister(s);
                    JOptionPane.showMessageDialog(this, "La suppression est bien effecturée","Suppression",JOptionPane.OK_OPTION);
                    nouveau();
              } 
              break; 
          }
        }
    }
